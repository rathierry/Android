public class Page
{
    Integer size;
    Integer totalElements;
    Integer totalPages;
    Integer number;

    public Page() {
    }

    public Integer getSize() {
        return size;
    }

    public Integer getTotalElements() {
        return totalElements;
    }

    public Integer getTotalPages() {
        return totalPages;
    }

    public Integer getNumber() {
        return number;
    }
}
